/// Classe pessoa.
///
/// Esta classe representa uma pessoa.
class Person {
  /// O nome da pessoa.
  String name;

  /// A idade da pessoa.
  int age;

  /// Cria uma nova instância da classe Person.
  Person(this.name, this.age);
}

/// Esta função retorna a soma de dois números inteiros.
int sum(int a, int b) {
  return a + b;
}
